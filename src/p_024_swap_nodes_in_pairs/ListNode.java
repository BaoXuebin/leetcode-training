package p_024_swap_nodes_in_pairs;

/**
 * @author BaoXuebin
 * @since 2021/10/31
 */
public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
